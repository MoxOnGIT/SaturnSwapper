    private readonly ISaturnAPIService _sataurnAPIService;
    public LocalizationResourceService(ISaturnAPIService saturnAPIService)
    {
        _sataurnAPIService = saturnAPIService;

        CultureInfo ci = CultureInfo.InstalledUICulture;
        Logger.Log("Default Language Info:");
        Logger.Log("* English Name: " + ci.EnglishName);

        if (ci.EnglishName.ToLower().Contains("english"))
            SelectedLanguage = Languages.English;
        else if (ci.EnglishName.ToLower().Contains("italian"))
            SelectedLanguage = Languages.Italian;
        else if (ci.EnglishName.ToLower().Contains("spanish"))
            SelectedLanguage = Languages.Spanish;
        else if (ci.EnglishName.ToLower().Contains("portuguese"))
            SelectedLanguage = Languages.Portuguese;
        else if (ci.EnglishName.ToLower().Contains("french"))
            SelectedLanguage = Languages.French;
        else if (ci.EnglishName.ToLower().Contains("german"))
            SelectedLanguage = Languages.German;
        else if (ci.EnglishName.ToLower().Contains("russian"))
            SelectedLanguage = Languages.Russian;
        else if (ci.EnglishName.ToLower().Contains("ukrainian"))
            SelectedLanguage = Languages.Ukrainian;
        else
            SelectedLanguage = Languages.English;
    }

